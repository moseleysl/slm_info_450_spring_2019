// This program converts gallons to liters.

#include <iostream>
using namespace std;

int main()
{
	int gallon;
	int liters;

	cout << "Enter number of gallons: ";
	cin >> gallons; // this inputs from the user

	liters = gallons * 4; // converts to liters

	cout << "Literss: " << liters;

	return 0;
}